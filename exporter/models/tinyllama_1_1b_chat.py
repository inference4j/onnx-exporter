import shutil
from pathlib import Path

from exporter.base import ExportedModel
from exporter.registry import register


class TinyLlama_1_1B_Chat(ExportedModel):
    name = "tinyllama-1.1b-chat"
    repo_id = "inference4j/tinyllama-1.1b-chat"

    def stage(self, staging_dir: Path) -> None:
        import numpy as np
        import onnxruntime as ort
        from huggingface_hub import hf_hub_download
        from optimum.exporters.onnx import main_export

        model_id = "TinyLlama/TinyLlama-1.1B-Chat-v1.0"

        # Export with optimum (merged model with KV cache, FP16 weights)
        print("  Exporting TinyLlama-1.1B-Chat to ONNX (FP16) with KV cache...")
        main_export(
            model_name_or_path=model_id,
            output=staging_dir,
            task="text-generation-with-past",
            no_post_process=False,
            dtype="fp16",
        )

        # Rename merged model to model.onnx
        target = staging_dir / "model.onnx"
        if not target.exists():
            merged = staging_dir / "decoder_model_merged.onnx"
            if merged.exists():
                # Also rename the external data file if present
                merged_data = staging_dir / "decoder_model_merged.onnx_data"
                if merged_data.exists():
                    merged_data.rename(staging_dir / "model.onnx_data")
                    print("  Renamed decoder_model_merged.onnx_data -> model.onnx_data")
                merged.rename(target)
                print("  Renamed decoder_model_merged.onnx -> model.onnx")

                # Update the external data reference inside the ONNX file
                import onnx
                model = onnx.load(str(target), load_external_data=False)
                for tensor in model.graph.initializer:
                    if tensor.data_location == onnx.TensorProto.EXTERNAL:
                        for entry in tensor.external_data:
                            if entry.key == "location" and "decoder_model_merged" in entry.value:
                                entry.value = "model.onnx_data"
                onnx.save(model, str(target))
                print("  Updated external data references in model.onnx")
            else:
                for candidate in sorted(staging_dir.glob("*.onnx")):
                    candidate.rename(target)
                    print(f"  Renamed {candidate.name} -> model.onnx")
                    break

        # Clean up intermediate model files generated by optimum
        for f in list(staging_dir.glob("decoder_model*")):
            f.unlink()
            print(f"  Removed {f.name}")

        # Copy tokenizer.json and config.json from HuggingFace
        # TinyLlama uses SentencePiece BPE via tokenizer.json
        for filename in ("tokenizer.json", "config.json"):
            dst = staging_dir / filename
            if not dst.exists():
                src = hf_hub_download(repo_id=model_id, filename=filename)
                shutil.copy2(src, dst)
                print(f"  Copied {filename}")

        # Remove non-essential files generated by optimum
        for name in ("generation_config.json", "tokenizer_config.json",
                     "special_tokens_map.json", "added_tokens.json"):
            p = staging_dir / name
            if p.exists():
                p.unlink()

        # Validate export
        print("  Validating export...")
        session = ort.InferenceSession(str(target))
        input_names = [i.name for i in session.get_inputs()]
        output_names = [o.name for o in session.get_outputs()]
        print(f"    Inputs ({len(input_names)}): {input_names[:6]}...")
        print(f"    Outputs ({len(output_names)}): {output_names[:4]}...")

        assert "input_ids" in input_names
        assert "attention_mask" in input_names
        assert any("past_key_values" in n for n in input_names), \
            f"No past_key_values in inputs: {input_names}"
        assert "logits" in output_names
        assert any("present" in n for n in output_names), \
            f"No present in outputs: {output_names}"

        # Count KV cache layers
        kv_layers = sum(1 for n in input_names
                        if n.startswith("past_key_values") and n.endswith(".key"))
        print(f"    KV cache layers: {kv_layers}")

        # Quick inference test
        prompt_ids = np.array([[1]], dtype=np.int64)  # <s> BOS token
        attention = np.ones_like(prompt_ids)
        feeds = {"input_ids": prompt_ids, "attention_mask": attention}

        # Add position_ids if expected
        if "position_ids" in input_names:
            feeds["position_ids"] = np.zeros_like(prompt_ids)

        # Add empty KV cache (use FP16 if the model expects it)
        for inp in session.get_inputs():
            if inp.name.startswith("past_key_values"):
                shape = list(inp.shape)
                shape = [s if isinstance(s, int) else 0 for s in shape]
                shape[0] = 1  # batch size
                dtype = np.float16 if inp.type == "tensor(float16)" else np.float32
                feeds[inp.name] = np.zeros(shape, dtype=dtype)

        results = session.run(None, feeds)
        logits = results[0]
        print(f"    Logits shape: {logits.shape}")
        assert logits.shape[-1] == 32000, \
            f"Expected vocab size 32000, got {logits.shape[-1]}"
        print("  Validation OK")

        # Report total model size
        total_mb = sum(
            f.stat().st_size for f in staging_dir.glob("model.onnx*")
        ) / (1024 * 1024)
        print(f"  Export complete: {total_mb:.0f} MB total (FP16)")

